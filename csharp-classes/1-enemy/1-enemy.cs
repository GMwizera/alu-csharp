namespace Enemies
{
    /// <summary>
    /// Defines a zombie enemy.
    /// </summary>
    public class Zombie
    {
        /// <summary>
        /// The health points of the zombie.
        /// </summary>
        public int health;

        /// <summary>
        /// Initializes a new instance of the <see cref="Zombie"/> class
        /// with its health set to 0.
        /// </summary>
        public Zombie()
        {
            health = 0;
        }
    }
}
